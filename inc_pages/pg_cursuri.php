    <li>realizarea de armonii de culori</li>
    <li>corecturi la nivelul fetei</li>
    <li>machiaj de zi</li>
    <li>machiaj de zi business</li>
    <li>machiaj banana inchisa</li>
    <li>machiaj pin-up</li>
    <li>machiaj smokey eyes</li>
    <li>machiaj cat eyes</li>
    <li>machiaj de mireasa clasic</li>
    <li>machiaj de infrumusetare si pentru camuflarea defectelor</li>
    <li>aplicarea de gene banda si smocuri</li>

  </ul>
  <br />
<strong>MODULUL 2 – MACHIAJ SPECIAL – 2500 Lei<br>
Durata: 2 saptamani (luni-vineri) 60 ore</strong><br />
<p style="color:#f00">*Curs destinat doar persoanelor care au in prealabil un curs de baza in machiaj.</p><br /><br />
In cadrul acestui modul se abordeaza urmatoarele teme:
 
<ul>
  <li>In partea teoretica se vor studia notiuni referitoare la lumina si temperatura sa, notiuni de fotografie, diferenta dintre machiajul vizualizat cu ochiul liber si cel vazut prin intermediul unei lentile foto (tipuri de lentile), cum sa iti pregatesti o propunere de machiaj (lucrul cu face chart).</li>
  <li>Bronzing / Contouring make-up / Glossy eyes</li>
  <li>Lifting make-up - Machiaj pentru tenul matur</li>
  <li>Smokey eyes contrastat realizat in tehnica creionului si pigmenti</li>
  <li>Glam cat eyes make-up / Smokey cat eyes make-up / Glitter</li>
  <li>Arabic make-up</li>
  <li>Linia italiana (cut crease)</li>
  <li>Monocromatic make-up - Excentric colors</li>
  <li>Glam bridal make-up / Tehnica strobing / Glitter</li>
</ul>

<p>Materialele (produse/pensule) cu care se lucreaza sunt asigurate de Melkior. Se folosesc numai produse Melkior.<br>
Practica se efectueaza, de cele mai multe ori, cursant pe cursant.<br><br>
Dupa fiecare Modul se sustine examen si se elibereaza Certificat de absolvire recunoscut de Ministerul Muncii si Ministerul Educatiei, precum si in Uniunea Europeana.
In ziua examenului se achita o taxa de inscriere in valoare de 100 lei.</p>
