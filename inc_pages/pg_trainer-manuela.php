      <h3 align="left">Manuela Gisca</h3>
       Numele meu este Manuela Gisca, sunt make-up artist si trainer la Scoala de make-up Melkior. <br />
Intamplator sau nu, pasiunea mea pentru machiaj am descoperit-o inca din   adolescenta, odata cu tusul pentru ochi, primul produs care mi-a atras   atentia si pe care il folosesc si acum cu aceeasi placere. <br />
Rand pe rand am descoperit produse noi si astfel, cu trecerea timpului,   am invatat sa le folosesc corect atat pe mine cat si pe altii. <br />
Fiind absolventa a Universitatii Nationale de Arte am descoperit ca cea   mai buna metoda de a imbina toate elementele unui limbaj plastic este   insasi arta machiajului. <br />
Jocul si imbinarea texturilor, intensitatile culorilor, modelarea   umbrelor si luminilor, desenul, simetria si compozitia sunt elemente   fara de care nu se poate realiza un machiaj reusit si totodata elemente   ce definesc stiluri si concepte, feluri in care fiecare dintre noi,   make-up artisti sau doar pasionati putem realiza look-uri inedite in   momentele cheie ale vietii noastre sau pur si simplu pentru bunastarea   de zi cu zi. <br />
Personal consider cat se poate de adevarat citatul lui Yves Saint   Laurent : &laquo;Frumusetea naturala este cea mai de pret, insa pentru asta ai   nevoie de machiaj&raquo;. <br />
Ceea ce imi place cel mai mult in aceasta meserie este faptul ca poti   crea emotii si sentimente, iar faptul ca pot totodata sa impart   cunostintele mele despre machiaj la orele de curs este cea mai mare   satisfactie pe care o poate avea orice make-up artist. <br />
Experienta mea in acest domeniu a cunoscut sedinte foto (reviste,   campanii, editoriale de moda), filmari (atat videoclipuri cat si   televiziune), show-uri de moda si foarte multe evenimente. <br />
Totodata am cunoscut oameni cat se poate de frumosi, printre care amintesc de Iulia Albu, Stephan Pelger sau Camelia Sucu. <br />
Insa cel mai important lucru ramane Scoala de make-up Melkior, scoala la care am onoarea sa fiu profesor. <br />
Va astept cu drag sa impartasim toate tainele acestei meserii
