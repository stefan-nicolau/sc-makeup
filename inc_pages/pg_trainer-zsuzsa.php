           <h3 align="left">Zsuzsa Hidi</h3>
           Numele meu este <strong><em>Zsuzsa (Juja) Hidi</em></strong> si sunt make-up artist si trainer la Scoala de Make-up Melkior.<br />
           <br />
           Arta machiajului m-a fascinat intotdeauna, ca si make-up artist ai satisfactia de a crea emotii, bucurie.<br />
           <br />
           In anul 2008  am decis sa ma inscriu la cursurile de machiaj profesional ale scolii <strong>Kruger Brent Agency</strong>, iar in anul 2009 am absolvit-o obtinand o diploma de Make-up Artist,   recunoscuta atat pe plan national, cat si international. Dupa acest curs   am participat la diferite <strong>workshop-uri</strong> (<em>tehnica creionului</em>, <em>tehnica acuarelei</em>, <em>tehnica aerografului</em>).<br />
           <br />
           Portofoliul meu cuprinde: <strong>machiaj pentru televiziune</strong> (emisiuni tv, spoturi comerciale, videoclipuri), <strong>machiaj foto</strong> (pictoriale de moda, reviste, mirese), <strong>machiaj fashion</strong> (prezentari de moda, evenimente), <strong>machiaj efecte speciale</strong> (film, teatru, sedinte foto) precum si <strong>machiaj de beauty</strong> pentru diverse evenimente, sedinte foto etc. </div>
